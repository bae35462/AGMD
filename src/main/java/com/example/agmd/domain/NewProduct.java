package com.example.agmd.domain;

public class NewProduct {
	private String npId;
	private String npPrice;
	private String npName;
	private String npAttr;
	private String userId;
	public String getNpId() {
		return npId;
	}
	public void setNpId(String npId) {
		this.npId = npId;
	}
	public String getNpPrice() {
		return npPrice;
	}
	public void setNpPrice(String npPrice) {
		this.npPrice = npPrice;
	}
	public String getNpName() {
		return npName;
	}
	public void setNpName(String npName) {
		this.npName = npName;
	}
	public String getNpAttr() {
		return npAttr;
	}
	public void setNpAttr(String npAttr) {
		this.npAttr = npAttr;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}
